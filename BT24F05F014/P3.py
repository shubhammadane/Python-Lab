# Variables and Data Types in Python

# Integer
a = 10
print("Integer:", a, type(a))

# Float
b = 3.14
print("Float:", b, type(b))

# String
c = "Hello"
print("String:", c, type(c))

# Boolean
d = True
print("Boolean:", d, type(d))

# List (mutable)
e = [1, 2, 3]
print("List:", e, type(e))

# Tuple (immutable)
f = (4, 5, 6)
print("Tuple:", f, type(f))

# Dictionary
g = {"name": "Yadvi", "age": 20}
print("Dictionary:", g, type(g))

# Type casting
x = "100"
y = int(x)
print("Converted to int:", y, type(y))
