a = 10
b = 3

# Arithmetic operations
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Comparison operations
print("Equal:", a == b)
print("Greater:", a > b)

# Logical operations
print("AND:", (a > 5 and b < 5))
print("OR:", (a > 5 or b > 5))

# Assignment operation
a += 5
print("After += :", a)