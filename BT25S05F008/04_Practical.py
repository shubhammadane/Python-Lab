a, b = 10, 3

# Arithmetic
print("Arithmetic:", a+b, a-b, a*b, a/b, a//b, a%b, a**b)

# Comparison
print("Comparison:", a>b, a<b, a==b, a!=b)

# Assignment
c = 5; c += 2; print("Assignment:", c)

# Logical
print("Logical:", True and False, True or False, not True)

# Bitwise
print("Bitwise:", a&b, a|b, a^b, ~a, a<<1, a>>1)

# Identity
x = [1,2]; y = x
print("Identity:", x is y, x is not y)