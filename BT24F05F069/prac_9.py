# String
s = "Programming"
print(s.upper())
print(s[4])

# List
lst = [10, 20, 30]
lst.append(40)
print(lst)

# Tuple
tup = (5, 10, 15)
print(tup[2])

# Set
st = {10, 20, 30}
st.add(40)
print(st)

# Set operations
a = {10, 20, 30}
b = {30, 40, 50}
print("Union:", a | b)
print("Intersection:", a & b)