# Student Information Program 
# Variables with different data types
# practical 3: Variables
name = input("Enter student name: ")      # string
age = int(input("Enter age: "))           # integer
marks = float(input("Enter marks: "))     # float

# Boolean variable
is_pass = marks >= 40

# Displaying information
print("\n--- Student Details ---")
print("Name:", name)
print("Age:", age)
print("Marks:", marks)

print("Pass Status:", is_pass)