# Numeric data types
age = 20                  # int
height = 5.9              # float
complex_number = 2 + 3j   # complex

# Text and boolean
name = "Aarav"            # str
is_student = True         # bool

# Collection data types
marks = [85, 90, 78]                      # list (mutable)
subjects = ("Math", "Science", "English")  # tuple (immutable)
unique_numbers = {1, 2, 3, 2, 1}          # set (unique values)
student_info = {"id": 101, "city": "Delhi"}  # dict (key-value pairs)

# Special data type
middle_name = None        # NoneType

print("Value and Data Type Examples")
print("-" * 35)
print("age =", age, "| type:", type(age))
print("height =", height, "| type:", type(height))
print("complex_number =", complex_number, "| type:", type(complex_number))
print("name =", name, "| type:", type(name))
print("is_student =", is_student, "| type:", type(is_student))
print("marks =", marks, "| type:", type(marks))
print("subjects =", subjects, "| type:", type(subjects))
print("unique_numbers =", unique_numbers, "| type:", type(unique_numbers))
print("student_info =", student_info, "| type:", type(student_info))
print("middle_name =", middle_name, "| type:", type(middle_name))
