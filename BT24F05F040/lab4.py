#arithmetic operators
a, b = 10, 3
print(a + b)   # Addition
print(a - b)   # Subtraction
print(a * b)   # Multiplication
print(a / b)   # Division 
#relational operators
x, y = 5, 10
print(x == y)  
print(x != y) 
print(x > y)  
print(x < y)  
#logical operators
p, q = True, False
print(p and q) 
print(p or q)  
print(not p)  
#assignment operators
val = 10      
val +=5
print(val)    
